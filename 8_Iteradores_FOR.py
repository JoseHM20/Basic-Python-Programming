# MANEJO DE ITERADORES FOR

# Los ciclos FOR funcionan con un numero limitado de veces

Alumnos = ["Pablo", "Juan", "Gerardo", "Luis", "Cesar", "Pedro", "Carlos", "Mauricio"]

# Iterador FOR
for Alumno in Alumnos: # Imprimir todos los valores de forma automatica
    print(Alumno)
    # El ciclo FOR concluye hasta recorrer todos los valores de la lista

print("El ciclo acabo")


